#include "fdf.h"

void	ft_print_matl(long double **mat, int max_width)
{
	int	x;
	int	y;

	y = 0;
	while (mat[y])
	{
		x = 0;
		while (x < max_width)
		{
			printf("mat[%d][%d] : %Lf\n", y, x, mat[y][x]);
			x++;
		}
		write(1, "\n", 1);
		y++;
	}
}
